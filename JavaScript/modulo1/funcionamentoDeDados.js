// Declarando as variáveis com valores Undefined

let numero;
let aprovado;
let nome;
let simbolo;
console.log(numero);
console.log(aprovado);
console.log(nome);
console.log(simbolo);

// Atribuindo valores nulos as variáveis declaradas
numero = null;
aprovado = null;
nome = null;
simbolo = null;
console.log(numero);
console.log(aprovado);
console.log(nome);
console.log(simbolo);

// Atribuindo valores do tipo Number, Boolean, String e Symbol
numero = 10;
aprovado = true;
nome = "Alex";
simbolo = Symbol("Meu simbolo");
console.log(numero);
console.log(aprovado);
console.log(nome);
console.log(simbolo);
